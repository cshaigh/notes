    Alerts
    ======

    Target: What resource and where
    Criteria: What specific action
    Details: Who, when, where how
    Action Group: Who to inform and how to inform them

    Portal Process
    --------------

    1. Go to resource group
    2. Select Monitoring -> Alerts
    3. Select + New alert rule
    4. Select Resource, then Done
    5. Select Condition
    6. Define / select Action Groups (people who receive alert)
    7. Define / configure Alert Details

