    Redeploying with a Template
    ===========================

    * Not all resources can be redeployed
    * Must change the name of some resources to avoid duplicates
    * Very effective way to redeploy resources

    Portal Process
    --------------

    1. Go to source resource group
    2. Select Settings -> Automation script
    3. Select Deploy
    4. Specify new Subscription, Resource Group, Location as needed
    5. Specify new Sites, Config, Serverfarms and Host settings as needed (avoid duplicating unique names)
    6. Agree to terms and conditions
    7. Click Purchase to start the redeployment

