    Metrics
    =======

    Resource Group: Where to look at the metric
    Resource Type: The type of resource to look at
    Available Metrics: What specifics about the metric
    Chart: Graphic display of metric

    Portal Process
    --------------

    1. Select Resource Group
    2. Select Monitoring -> Metrics
    3. Add / configure specific metrics for specific resources in specific resource groups and view the corresponding chart

